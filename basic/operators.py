# Arithmetic Operators

#expression: Addition

a = 10
b = 5
sum_result = a + b
print("Addition:", sum_result)

#joining strings
greeting = "Hello, "
name = "Alice"
full_greeting = greeting + name
print("String Concatenation:", full_greeting)

name = 'Bob'
print("New Name:", name)


#multiply strings
repeat_greeting = greeting * 3
print("String Repetition:", repeat_greeting)

